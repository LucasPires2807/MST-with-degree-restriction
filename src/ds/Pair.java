package ds;

public class Pair{

    private final char element0;
    private final char element1;

    public Pair(char element0, char element1) {
        this.element0 = element0;
        this.element1 = element1;
    }

    public char getElement0() {
        return element0;
    }

    public char getElement1() {
        return element1;
    }

}